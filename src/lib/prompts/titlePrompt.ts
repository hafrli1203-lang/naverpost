/**
 * 후보 키워드가 선택된 카테고리에 맞는지 LLM이 판정하게 하는 분류 프롬프트.
 * 하드코딩 정규식 대신 카테고리 메타데이터(이름 + 세부주제)로 판단하며,
 * 눈정보·안경이야기는 특정 제품에 매이지 않게 폭넓게 본다.
 */
export function buildCategoryFitPrompt(params: {
  categoryName: string;
  subcategories: string[];
  candidates: Array<{ mainKeyword: string; subKeyword1: string; subKeyword2: string }>;
}): string {
  const { categoryName, subcategories, candidates } = params;
  const lines = candidates
    .map((c, i) => `${i + 1}. ${c.mainKeyword} / ${c.subKeyword1} / ${c.subKeyword2}`)
    .join("\n");

  return `당신은 안경원 블로그 키워드를 카테고리에 맞게 분류하는 검수자입니다.

[선택된 카테고리] ${categoryName}
[이 카테고리의 세부 주제] ${subcategories.join(", ")}

[6개 카테고리 구분 기준]
- 안경테: 안경 프레임(소재/얼굴형/피팅/코패드/무게 등)
- 안경렌즈: 렌즈 제품(도수/코팅/블루라이트/변색/압축/고굴절/어린이근시렌즈 등)
- 콘택트렌즈: 콘택트렌즈(건조/충혈/난시/원데이/착용시간/관리 등)
- 누진다초점: 노안 교정 렌즈(누진/다초점/노안안경/돋보기/사무용·실내용 누진 등)
- 눈정보: 눈 건강·시력·증상 일반 정보(특정 제품에 매이지 않음). 폭넓게 인정.
- 안경이야기: 안경 관리·수리·세척·보관·생활 이야기(특정 제품에 매이지 않음). 폭넓게 인정.

아래 후보(메인 / 서브1 / 서브2) 중 "${categoryName}" 카테고리에 자연스럽게 속하는 번호만 고르세요.
- 다른 카테고리의 제품/주제는 제외하세요. (예: 누진다초점에 일반 눈피로·안구건조·콘택트렌즈는 제외)
- 단 눈정보·안경이야기 카테고리라면 특정 제품에 얽매이지 말고 폭넓게 인정하세요.
- 애매하면 어느 카테고리 핵심 주제에 더 가까운지로 판단하세요.

[후보]
${lines}

JSON만 출력: {"keep":[적합한 번호들]}`;
}

/**
 * 최종 제목 후보의 어색한 표현·오타·비문만 다듬는 교정 프롬프트.
 * 키워드(메인/서브)는 절대 바꾸지 않고, 문장만 자연스럽게 한다.
 */
export function buildTitlePolishPrompt(
  candidates: Array<{
    title: string;
    mainKeyword: string;
    subKeyword1: string;
    subKeyword2: string;
  }>,
  corpusTitles: string[] = []
): string {
  const corpusSection =
    corpusTitles.length > 0
      ? `\n[실제 업종 제목 용어 기준]\n${corpusTitles
          .slice(0, 15)
          .map((title) => `- ${title}`)
          .join("\n")}\n※ 다듬을 때 용어·분류 명칭·표현 수준은 위 실제 제목들이 쓰는 수준을 따르세요.\n`
      : "";
  const lines = candidates
    .map(
      (c, i) =>
        `${i + 1}. title=${c.title} / main=${c.mainKeyword} / sub1=${c.subKeyword1} / sub2=${c.subKeyword2}`
    )
    .join("\n");

  return `당신은 네이버 블로그 제목을 다듬는 한국어 교정 에디터이자 안경 광학 사실 검수자입니다.
아래 제목들에서 (1) 어색한 표현·오타·비문·부자연스러운 조사를 고치고 (2) 사실이 아닌 전제를 바로잡으세요.

[사실성 검수 — 최우선]
- 광학적으로 성립하지 않거나 검증할 수 없는 인과 주장이 제목에 있으면 main 키워드를 보존한 채 상황형으로 다시 쓰세요.
  예: main="변색렌즈 도수"인 "변색렌즈 도수 그대로 두면 야외에서 흐림이 반복됩니다"(지어낸 인과) → "변색렌즈 도수를 그대로 써도 되는지 야외 흐림 때문에 고민될 때"
- 사실 조각을 뭉갠 명사구는 비문입니다. 예: "다리가 자주 벌어지는 얼굴형에서 확인할 힌지 구조" → "안경 다리가 자꾸 벌어질 때 확인할 부분"
- 등급이 나뉘는 개념을 한 등급명으로 뭉뚱그린 제목은 상위 개념으로 고치세요. 안경렌즈 두께 등급은 중굴절(1.56) 고굴절(1.60) 초고굴절(1.67) 특고굴절(1.74)로 나뉩니다. 예: "고굴절렌즈 두께를 고를 때 살펴볼 점"(main이 그 등급명일 때는 유지하되 어색하면) → "고굴절렌즈 등급별 두께가 궁금할 때"처럼 등급 비교가 읽히게.
- 제목은 원인-결과 단정("~하면 ~됩니다", "~수록 ~해지는")이 아니라 검색자가 겪는 상황·궁금증까지만 담아야 합니다. 답은 본문 몫입니다.

[제목 목록]
${lines}
${corpusSection}
[교정 규칙]
- main 키워드의 두 단어는 제목 안에서 "순서대로 인접하게" 들어가야 합니다. 첫 단어 뒤에 자연스러운 조사(이/가/은/는/을/를/에/에서/의/도/로)는 붙여도 됩니다. 예: main="안경렌즈 얼룩"이면 "안경렌즈 얼룩"도 되고 "안경렌즈에 얼룩이 남을 때"도 됩니다.
- main 두 단어가 "코패드가 자꾸...관리"처럼 멀리 분리되어 있으면, 두 단어가 이어지도록 문장을 다시 쓰세요.
- 키워드 덩어리를 문두에 그대로 박고 절을 이어붙인 비문(예: "안경흘러내림 조정 선글라스가 땀날 때 내려오는 원인")은 키워드를 주어나 목적어로 녹여 사람이 말하는 문장으로 고치세요.
- 단, main을 억지로 끼우느라 비문이 되면 안 됩니다. 자연스러움이 우선입니다.
- 의미와 소재는 그대로 두고, 문장만 매끄럽게 다듬으세요. 새 소재나 새 관점을 만들지 마세요.
- 명백한 오타·비문을 고치세요. 예: "동보기안경"→"돋보기안경", "오래 끼리는"→"오래 쓰는".
- 어색한 명사 조합을 자연스럽게: 예: "업무 중 사이가 느껴질"→"업무 중 더 불편하게 느껴질".
- 제목 속 전문 수치·용어(455nm, PD, 아베수, 색수차, 명시야, 누진대, 가입도, 아이포인트 등)는 손님이 쓰는 일상어로 바꾸세요. 예: "455nm 표기가 보이면"→"차단 표시가 헷갈릴 때", "명시야가 중요한 상황"→"선명하게 보이는 범위가 중요한 상황".
- 쉼표(,)와 이모지 사용 금지. 접속 어미로 자연스럽게 이으세요.
- 제목 길이 12~32자. 길면 핵심만 남기고 줄이세요(지역명은 사용자가 별도로 붙입니다).
- 후보마다 끝맺음이 서로 다르도록 유지하세요(같은 끝맺음 반복 금지). 특히 "~할 때"가 3개 이상이면 일부를 "~다면"/"~읽는 법"/질문형 등으로 바꾸세요.
- 사람 말투 우선 (도식 명사 스택 금지): "맡기기 전 확인 순서" "~까지 살피는 법" "점검하는 순서"처럼 명사를 쌓아 딱딱하게 끝내는 제목은, 검색자가 실제로 머릿속에 떠올리는 말로 풀어 쓰세요. 예: "안경테 변형 맡기기 전 확인 순서" → "안경테가 휘었는데 맡기기 전에 뭘 볼까". 소리 내어 읽어 사람이 안 하는 말투면 고칩니다.
- "순서/법/점/부분/기준" 같은 명사형 마무리가 여러 후보에 반복되면 일부는 평서·조건·질문형(~할까 / ~한다면 / ~하면 좋을까 / ~괜찮을까)으로 바꿔 후보 전체의 결이 사람 글처럼 들리게 하세요. 단 main 키워드 인접·길이·쉼표금지는 그대로 지킵니다.
- 이미 자연스러운 제목은 그대로 두세요.

JSON만 출력하세요. 입력과 같은 개수, 같은 index로:
{"results":[{"index":1,"title":"다듬은 제목"}, ...]}`;
}

// 개수 안정화용: 제목이 없는 실볼륨 시드(키워드만 있음)에 자연스러운 제목을 1개씩 생성한다.
