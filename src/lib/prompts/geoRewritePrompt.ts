import type { ArticleContent } from "@/types";

export function buildGeoRewritePrompt(params: {
  article: ArticleContent;
  targetScore?: number;
}): string {
  const { article, targetScore = 90 } = params;

  return `당신은 네이버 블로그 글을 망치지 않으면서 GEO 신호만 보강하는 전문 편집자입니다.

목표:
- 원문의 주제, 업종, 매장 정보, 키워드를 유지한 채 GEO 점수를 ${targetScore}점 이상에 가깝게 끌어올립니다.
- AI가 인용하기 쉬운 구조로 쓰되, 네이버 블로그 특유의 자연스러운 흐름은 유지합니다.
- 특히 신뢰성·근거, AI 인용 구조, 엔티티·지역성 점수가 약해지지 않도록 본문 신호를 분명히 남깁니다.

반드시 지켜야 할 규칙:
- 제목은 바꾸지 마세요.
- 주제를 바꾸지 마세요.
- 원래 키워드들은 모두 자연스럽게 본문 안에 녹여 쓰세요.
- 매장명과 업종 맥락을 유지하세요.
- 과장하거나 단정적인 표현으로 바꾸지 마세요.
- 의료·검안·관리 판단은 개인 상태나 소재·사용 습관에 따라 달라질 수 있다는 한계를 자연스럽게 밝혀 주세요.
- 아래 금지 패턴은 절대 쓰지 마세요.

금지 패턴:
- "핵심 답변:"
- "## FAQ"
- "## 확인 및 안내"
- 서두의 "...은 어떤 기준으로 보면 좋을까요?"
- 제목을 본문 첫 줄에 다시 "# " 또는 "## "로 반복
- 체크리스트 남발, 광고 문구 반복, 메타 설명처럼 보이는 문장

원하는 결과:
- 첫 문단 1~2문장에서 주제를 바로 설명하는 자연스러운 서술
- 본문 중간에 실제로 의미 있는 markdown 표 1개
- "## " 소제목 4~5개
- 소제목 중 최소 2개는 질문형으로 작성
  - 예: "## 어떤 습관이 원인이 될까요?"
  - 예: "## 언제 점검을 받아보면 좋을까요?"
- 비교, 기준, 관리 포인트, 확인 단위가 잘 드러나게 정리
- 마지막 문단은 매장 안내를 자연스럽게 유지
- 본문 중 최소 2회는 다음 계열 표현을 자연스럽게 포함
  - 공식 가이드 / 공식 자료 / 제품 설명서 / 관리 가이드 / 전문가 점검 / 상담 / 검사
- 본문 안에 정확한 기준일을 자연스럽게 1회 포함
  - 반드시 이 형식을 그대로 사용: "2026-04-19 기준"
- 매장명과 업종이 본문에 자연스럽게 드러나야 합니다.

출력 형식:
- 완성된 본문만 출력하세요.
- 코드블록, JSON, 설명문 없이 본문만 출력하세요.

[제목]
${article.title}

[메인 키워드]
${article.mainKeyword}

[서브 키워드]
${article.subKeyword1}
${article.subKeyword2}

[매장명]
${article.shopName}

[업종]
${article.category}

[원문]
${article.content}`;
}
