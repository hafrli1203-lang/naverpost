type ToneType = "standard" | "friendly" | "casual";
type CharCountType = 1000 | 1500 | 2000 | 2500;

export function getToneGuide(tone?: string): string {
  switch (tone) {
    case "friendly":
      return `- 본문(내용): "~거든요" "~이에요" "~랍니다" "~죠" "~네요" 등 따뜻하고 다정한 구어체를 사용하세요.
- 독자에게 말을 거는 듯한 공감형 톤을 유지하세요.
- "혹시 이런 경험 있으신가요?" "사실 많은 분들이 모르시는데요" 같은 공감 표현을 활용하세요.
- 소제목(헤더): 본문보다 약간 단정하되 딱딱하지 않게 작성하세요.`;
    case "casual":
      return `- 본문(내용): "~요" "~예요" "~잖아요" "~거예요" "~인데요" 등 가볍고 부드러운 대화체를 사용하세요.
- 친구에게 설명하듯 편안한 톤을 유지하세요.
- "아 이거 진짜 중요한 부분인데요" "생각보다 간단해요" 같은 자연스러운 전환을 활용하세요.
- 소제목(헤더): 본문과 비슷한 가벼운 톤이되 핵심은 명확하게 작성하세요.`;
    case "standard":
    default:
      return `- 본문(내용): "~해요" "~하거든요" "~한답니다" "~까요" 등 옆에서 말하듯 구어체를 사용하세요.
- 전문적이면서도 친절한 안경사의 톤을 유지하세요.
- 소제목(헤더): 본문과 달리 전문적이고 신뢰감 있는 단정한 톤을 유지하세요.`;
  }
}

function getSectionCount(charCount: CharCountType): number {
  switch (charCount) {
    case 1000:
      return 2;
    case 1500:
      return 3;
    case 2000:
      return 4;
    case 2500:
      return 5;
    default:
      return 4;
  }
}

function buildSectionTemplate(count: number): string {
  const sections: string[] = [];
  for (let i = 1; i <= count; i++) {
    if (i === 1) {
      sections.push(`## (소주제 ${i} 제목 - 질문형: 검색자가 실제로 묻는 질문)
(첫 문장: 검색자가 궁금해한 문제에 40~80자로 바로 답하기)
(원리 설명: 왜 그런지 광학적/해부학적 배경을 쉽게 서술)`);
    } else if (i === 2) {
      sections.push(`## (소주제 ${i} 제목 - 서술형: 핵심 내용을 담백하게 요약)
(풀이: 일상적인 비유나 쉬운 사물에 빗대어 이해시키기)
(독자가 자기 상황과 비교할 수 있는 생활 장면 제시)`);
    } else if (i === count) {
      sections.push(`## (소주제 ${i} 제목 - 서술형: 안경원 점검 포인트 요약)
(안경원에서 확인할 항목: 도수·피팅·착용감·생활거리 등 제목과 관련된 점검 포인트)
(한계 인정 표현 포함)`);
    } else {
      sections.push(`## (소주제 ${i} 제목 - 서술형: 핵심 내용 요약)
(구체적인 해결책 또는 선택 기준 제시)
(방문 전 스스로 확인할 수 있는 신호와 매장에서 확인할 신호를 구분)`);
    }
  }
  return sections.join("\n\n");
}

